    private static Map<String, Object> keyMap = null;
    /** *//**
     * <p>
     * 获取私钥
     * </p>
     *
     * @param keyMap 密钥对
     * @return
     * @throws Exception
     */
    public static String getPrivateKey(Map<String, Object> keyMap)
            throws Exception {
        Key key = (Key) keyMap.get(PRIVATE_KEY);
        return Base64Util.encodeByte(key.getEncoded());
    }

    /** *//**
     * <p>
     * 获取公钥
     * </p>
     *
     * @param keyMap 密钥对
     * @return
     * @throws Exception
     */
    public static String getPublicKey(Map<String, Object> keyMap)
            throws Exception {
        Key key = (Key) keyMap.get(PUBLIC_KEY);
        return Base64Util.encodeByte(key.getEncoded());
    }
	public static void main(String[] args) throws Exception {
		Map<String, Object> keys = RSAUtils2.genKeyPair();
		RSAPublicKey pubKey = (RSAPublicKey)keys.get(PUBLIC_KEY);
		RSAPrivateKey priKey = (RSAPrivateKey)keys.get(PRIVATE_KEY);
    	System.out.println("公钥\r\n"+new String(pubKey.getEncoded()));
    	System.out.println("私钥\r\n"+new String(priKey.getEncoded()));
        String source = "春眠不觉晓，处处闻啼鸟，也来风雨声，花落知多少。";
        System.out.println("原文字：" + source);
        publicEnPrivateDe(source);//公钥加密私钥解密测试
        System.out.println("--------------------------");
        signVerify(source);//签名和签名验证测试
	}
	
	private static void signVerify(String source) throws Exception {
		byte[] pubEncodString = encryptByPublicKey(source.getBytes(), PUBLIC_KEY);
        System.out.println("公钥加密内容："+new String(pubEncodString));
        System.out.println("对加密内容 进行签名："+sign(pubEncodString, PRIVATE_KEY));
        boolean verify = verify(pubEncodString, PUBLIC_KEY, sign(pubEncodString, PRIVATE_KEY));
        System.out.println("验证签名是否成功："+verify);
        if(verify){
        	System.out.println("私钥解密："+new String(decryptByPrivateKey(pubEncodString, PRIVATE_KEY)));
        }else{
        	System.out.println("签名失败！");
        }
	}

	public static void publicEnPrivateDe(String source) throws Exception{
        byte[] pubEncodString = encryptByPublicKey(source.getBytes(), PUBLIC_KEY);
        System.out.println("公钥加密内容："+new String(pubEncodString));
        System.out.println("私钥解密："+new String(decryptByPrivateKey(pubEncodString, PRIVATE_KEY)));
	}
